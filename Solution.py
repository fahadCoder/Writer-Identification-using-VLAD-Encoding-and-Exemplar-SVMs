def assignments(descriptors, clusters):
    """ 
    compute assignment matrix
    parameters:
        descriptors: TxD descriptor matrix
        clusters: KxD cluster matrix
    returns: TxK assignment matrix
    """
    """
    The function computes the assignment of descriptors to clusters. It uses a brute-force matcher (cv2.BFMatcher) 
    with L2 norm to find the nearest cluster center for each descriptor. For each descriptor, the function matches it to the 
    closest cluster and stores the result in a binary assignment matrix, where each row represents a descriptor and each column 
    represents a cluster. The matrix contains True at the position corresponding to the nearest cluster for each descriptor. 
    Finally, the assignment matrix is returned
    """

    # Function to assign each descriptor to the nearest cluster center
    bf = cv2.BFMatcher(cv2.NORM_L2) # Brute force matcher using L2 norm for matching descriptors to clusters
    matches = bf.knnMatch(descriptors, clusters, k=1) # Match descriptors to clusters
     # create hard assignment
    assignment = np.zeros((len(descriptors), len(clusters)), dtype=bool) # Initialize assignment matrix
    for i, match in enumerate(matches): # Iterate over all matches
        assignment[i, match[0].trainIdx] = True # Assign the descriptor to the nearest cluster center
    return assignment # Return the assignment matrix

def vlad(files, mus, powernorm, gmp=False, gamma=1000):
    """
    compute VLAD encoding for each files
    parameters: 
        files: list of N files containing each T local descriptors of dimension
        D
        mus: KxD matrix of cluster centers
        gmp: if set to True use generalized max pooling instead of sum pooling
    returns: NxK*D matrix of encodings
    """
    """
    The function iterates over each file in the files list. For each file, it loads the local descriptors (T descriptors of dimension D).
        If the file has no descriptors, it appends a zero vector to the encodings.
        It then computes the assignment of descriptors to cluster centers using the assignments function.
        For each cluster, the residuals (differences between the descriptors and the cluster center) are computed.
        If gmp is True, it applies Ridge regression to the residuals.
        If gmp is False, it sums the residuals (standard VLAD).
        The resulting vectors for each cluster are concatenated to form the VLAD encoding for the file.
        
        Normalization:
        If powernorm is True, it applies power normalization (signed square root) to the encoding.
        Finally, the encoding is L2 normalized.

        It returns a Nx(K*D) matrix, where N is the number of files, K is the number of clusters, and D is the dimension of each descriptor.
    """


# Get the number of clusters and dimension of cluster centers
    K = mus.shape[0]
    D = mus.shape[1]
    encodings = []  # Initialize the encodings

    for f in tqdm(files):   # Iterate over all files
        # load local descriptors for this image
        # with gzip.open(f, 'rb') as ff:      # Open the file
        #     desc = cPickle.load(ff, encoding='latin1')  # Load the descriptors shape: T x D
        desc = loadDescsFromFile(f)

        if desc is None or len(desc) == 0:
            # no descriptors -> zero vector
            f_enc = np.zeros((K * D), dtype=np.float32)
            encodings.append(f_enc)
            continue

        desc = np.asarray(desc, dtype=np.float32)# Compute the assignments and initialize
        T, D = desc.shape

        # association / assignment matrix or vector (depends on your implementation)
        a = assignments(desc, mus)

        f_enc = np.zeros((K * D), dtype=np.float32) # Initialize the VLAD encoding

        for k in range(K):
            # select descriptors assigned to cluster k
            if a.ndim == 2:
                # one-hot / association matrix: T x K
                idx = np.where(a[:, k] > 0)[0]
            else:
                # vector of cluster indices: length T
                idx = np.where(a == k)[0]

            if idx.size == 0:
                # no descriptors for this cluster -> keep zeros
                continue

            # residuals for this cluster: (T_k x D)
            R = desc[idx, :] - mus[k]

            if gmp:
                # ----- Generalized Max Pooling (bonus part f) -----
                # use Ridge regression: encoding is coef_ (1 x D)
                # handle tiny clusters robustly
                if R.shape[0] == 1:
                    # fallback: just take that residual
                    v_k = R[0]
                else:
                    ridge = Ridge(
                        alpha=gamma,
                        solver='sparse_cg',
                        fit_intercept=False,
                        max_iter=500
                    )
                    # target vector of ones
                    y = np.ones(R.shape[0], dtype=np.float32)
                    ridge.fit(R, y)
                    v_k = ridge.coef_.ravel().astype(np.float32)
            else:
                # ----- Standard VLAD: sum pooling -----
                v_k = R.sum(axis=0)

            # put this cluster's vector into the global VLAD vector
            f_enc[k * D:(k + 1) * D] = v_k

        # c) power normalization (signed sqrt) if requested
        if powernorm:
            p = 0.5  # exponent for power normalization
            f_enc = np.sign(f_enc) * (np.abs(f_enc) ** p)

        # final â2 normalization
        norm = np.linalg.norm(f_enc)
        if norm > 0:
            f_enc = f_enc / norm

        encodings.append(f_enc.astype(np.float32))

    # Nx(K*D)
    return np.vstack(encodings)


   

def esvm(encs_test, encs_train, C=1000):
    """ 
    compute a new embedding using Exemplar Classification
    compute for each encs_test encoding an E-SVM using the
    encs_train as negatives   
    parameters: 
        encs_test: NxD matrix
        encs_train: MxD matrix

    returns: new encs_test matrix (NxD)
    """

    """
    The function first ensures that both encs_test and encs_train are NumPy arrays.
        It then creates a label vector y with the first entry as 1 (indicating a positive exemplar, i.e., the test encoding) and the rest as -1 (indicating the negatives, i.e., the training encodings).
        For each test encoding (indexed by i), it builds a training set where the test encoding is the positive sample (label 1) and the training encodings are the negative samples (label -1).
        A linear SVM classifier (LinearSVC) is trained on this set with a balanced class weight to handle the imbalance between positives and negatives, using the regularization parameter C=1000.
        The SVM's weight vector (coef_) is extracted, which represents the new embedding for the test encoding.
        The function applies this process in parallel to all test encodings using map (or optionally parmap if parallelism is set up).
        The new embeddings for all test encodings are collected and concatenated into a final matrix.
        The function returns a new NxD matrix, where N is the number of test samples and D is the dimensionality of the embeddings. Each row is the new embedding for a corresponding test sample.
    """


    # make sure we have numpy arrays
    encs_test = np.asarray(encs_test, dtype=np.float32)
    encs_train = np.asarray(encs_train, dtype=np.float32)

    N, D = encs_test.shape  # Get the number of test encodings and dimension
    M = encs_train.shape[0]     # Get the number of training encodings

    # set up labels: 1 positive (test sample), M negatives (all train)
    y = np.empty(M + 1, dtype=np.int32)
    y[0] = 1          # positive exemplar
    y[1:] = -1        # negatives

    def loop(i):
        # build training data for this exemplar SVM:
        # first row = current test vector (positive), rest = all train encodings (negatives)
        X = np.vstack((encs_test[i][None, :], encs_train))

        # train linear SVM with class_weight balanced, C=1000 as in the sheet
        clf = LinearSVC(C=C, class_weight='balanced', max_iter=10000)
        clf.fit(X, y)

        # weight vector is the new descriptor
        w = clf.coef_.ravel().astype(np.float32)

        # â2-normalize the weight vector
        norm = np.linalg.norm(w)
        if norm > 0:
            w = w / norm

        # return as 1xD for concatenation later
        return w[None, :]

    # parallel map over all test encodings
    # new_encs = list(parmap(loop, tqdm(range(N))))
    new_encs = list(map(loop, tqdm(range(N))))
    new_encs = np.concatenate(new_encs, axis=0)

    # return new encodings (NxD)
    return new_encs


def distances(encs):
    """ 
    compute pairwise distances 

    parameters:
        encs:  TxK*D encoding matrix
    returns: TxT distance matrix
    """

    """
    The function first ensures that encs is a NumPy array with the correct data type (float32).
        It then L2-normalizes each encoding (row-wise) by dividing each encoding by its L2 norm. This ensures that each 
        encoding has unit length, which is necessary for cosine similarity.
        Cosine similarity between two vectors is computed as the dot product of their L2-normalized forms. This is done 
        using np.dot(encs_norm, encs_norm.T), which computes the pairwise similarity matrix.
        The function then computes the pairwise distances by subtracting the cosine similarity from 1.0, as cosine distance
        is defined as 1 - cosine similarity.
        The diagonal of the distance matrix (which represents the distance of each encoding to itself) is filled with the 
        maximum float value to ensure that it is ignored in further calculations.
        The function returns a TxT distance matrix, where each entry (i, j) represents the cosine distance between encoding i and encoding j.
    """
   

    # ensure numpy array
    encs = np.asarray(encs, dtype=np.float32)

    # l2-normalize each encoding (row-wise)
    norms = np.linalg.norm(encs, axis=1, keepdims=True)
    norms[norms == 0] = 1.0  # avoid division by zero
    encs_norm = encs / norms

    # cosine distance = 1 - cosine similarity
    # cosine similarity is just dot product of l2-normalized vectors
    sim = np.dot(encs_norm, encs_norm.T)
    dists = 1.0 - sim

    # mask out distance with itself (set diagonal to max float)
    np.fill_diagonal(dists, np.finfo(dists.dtype).max)

    return dists

def evaluate(encs, labels):
    """
    evaluate encodings assuming using associated labels
    parameters:
        encs: TxK*D encoding matrix
        labels: array/list of T labels
    """

    """
    The evaluate function computes top-1 accuracy and mean Average Precision (mAP) for a set of encodings. It calculates 
    pairwise distances between encodings, sorts them, and checks how well the closest neighbors match the true class labels. 
    It then prints the top-1 accuracy and mAP values.
    """
    dist_matrix = distances(encs)   # compute distance matrix
    # sort each row of the distance matrix
    indices = dist_matrix.argsort() # sort each row of the distance matrix

    n_encs = len(encs)  # get the number of encodings

    mAP = []        # initialize mean average precision
    correct = 0     # initialize correct count
    for r in range(n_encs):     # iterate over all rows
        precisions = []     # initialize list of precisions
        rel = 0     # initialize relevance count
        for k in range(n_encs-1):       # iterate over all columns
            if labels[ indices[r,k] ] == labels[ r ]:       # check if the labels match
                rel += 1                                    # increment relevance count
                precisions.append( rel / float(k+1) )          # compute precision
                if k == 0:                                  # check if the first column is correct
                    correct += 1                            # increment correct count
        avg_precision = np.mean(precisions)                 # compute average precision
        mAP.append(avg_precision)                           # append average precision
    mAP = np.mean(mAP)                                      # compute mean average precision

    print('Top-1 accuracy: {} - mAP: {}'.format(float(correct) / n_encs, mAP))  # print top-1 accuracy and mAP



