def make_codebooks(files_train, n_codebooks=5, n_clusters=100,
                   max_desc=500000, overwrite=False, prefix='mus_cb'):
    """
    Train or load multiple codebooks. Returns list of KxD matrices.
    """
    codebooks = []
    for i in range(n_codebooks):
        fname = f'{prefix}{i}_K{n_clusters}.pkl.gz'
        if (not os.path.exists(fname)) or overwrite:
            print(f'[codebook {i+1}/{n_codebooks}] sampling descriptors...')
            # different seed/subset each time
            np.random.seed(100 + i)
            descs = loadRandomDescriptors(files_train, max_descriptors=max_desc)
            print(f'[codebook {i+1}] clustering...')
            # IMPORTANT: pass a different random_state
            kmeans = MiniBatchKMeans(
                n_clusters=n_clusters, batch_size=10000, random_state=100 + i
            )
            kmeans.fit(np.asarray(descs, dtype=np.float32))
            mus = kmeans.cluster_centers_.astype(np.float32)
            with gzip.open(fname, 'wb') as fOut:
                cPickle.dump(mus, fOut, -1)
        else:
            with gzip.open(fname, 'rb') as fIn:
                mus = cPickle.load(fIn)
        codebooks.append(mus)
    return codebooks

def multivlad(files, codebooks, powernorm=False, gmp=False, gamma=1.0):
    """
    Compute VLAD for each codebook and concatenate features.
    Returns: Nx(5*K*D)
    """
    enc_list = []
    for i, mus in enumerate(codebooks):
        print(f'  VLAD for codebook {i+1}/{len(codebooks)}')
        enc_i = vlad(files, mus, powernorm=powernorm, gmp=gmp, gamma=gamma)
        enc_list.append(enc_i)
    return np.concatenate(enc_list, axis=1).astype(np.float32)


def pca_whiten(train_X, test_X, out_dim=1000, subset=0):
    """
    Fit PCA(whiten=True) on train set (or subset), transform train and test.
    """
    train_X = np.asarray(train_X, dtype=np.float32)
    test_X  = np.asarray(test_X, dtype=np.float32)

    # choose subset for PCA fit (optional)
    fit_X = train_X
    if subset and subset < train_X.shape[0]:
        idx = np.random.choice(train_X.shape[0], subset, replace=False)
        fit_X = train_X[idx]

    # n_components must be <= min(n_samples, n_features)
    n_comp = min(out_dim, fit_X.shape[0], train_X.shape[1])

    pca = PCA(n_components=n_comp, whiten=True, svd_solver='randomized', random_state=42)
    pca.fit(fit_X)

    train_p = pca.transform(train_X).astype(np.float32)
    test_p  = pca.transform(test_X).astype(np.float32)

    # recommended for cosine distance
    train_p = normalize(train_p, norm='l2')
    test_p  = normalize(test_p, norm='l2')

    return train_p, test_p, pca

