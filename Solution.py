def dictionary(descriptors, n_clusters):
    """ 
    return cluster centers for the descriptors 
    parameters:
        descriptors: NxD matrix of local descriptors
        n_clusters: number of clusters = K
    returns: KxD matrix of K clusters
    """
    """
    The dictionary function performs clustering on a set of local descriptors to return cluster centers. It first ensures
      the descriptors are in a 2D NumPy array and checks that the number of descriptors is at least equal to the number of
        clusters. It then uses MiniBatchKMeans to fit the descriptors, with the number of clusters specified. After fitting, 
        the function returns the cluster centers, which form a dictionary of n_clusters clusters, each with the same 
        dimensionality as the original descriptors.
    """
    # ensure array and correct dtype
    descriptors = np.asarray(descriptors, dtype=np.float32)

    if descriptors.ndim != 2:
        raise ValueError("descriptors must be a 2D array of shape (N, D)")

    if descriptors.shape[0] < n_clusters:
        raise ValueError("Need at least n_clusters descriptors")

    # MiniBatchKMeans codebook (k = n_clusters)
    kmeans = MiniBatchKMeans(       # Use MiniBatchKMeans to compute cluster centers
        n_clusters=n_clusters,
        batch_size=10000,   
        random_state=42      
    )
    kmeans.fit(descriptors)     # Fit the descriptors

    # K x D matrix of cluster centers = dictionary D
    return kmeans.cluster_centers_      # Return the cluster centers


