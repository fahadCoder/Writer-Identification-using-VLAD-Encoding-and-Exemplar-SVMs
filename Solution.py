def computeDescs(filename):
    """
    Load image and compute SIFT descriptors at SIFT keypoints.
    Improvements:
      (1) set all keypoint angles to 0
      (2) Hellinger normalization: L1 normalize, then signed sqrt (NO L2 afterwards)
    Returns: Tx128 float32 array (may be empty)
    """
    img = cv2.imread(filename, cv2.IMREAD_GRAYSCALE)
    if img is None:
        raise FileNotFoundError(f"Could not read image: {filename}")

    sift = cv2.SIFT_create()

    kps = sift.detect(img, None)
    if not kps:
        return np.empty((0, 128), dtype=np.float32)

    # (1) set angles to 0
    for kp in kps:
        kp.angle = 0.0

    kps, desc = sift.compute(img, kps)
    if desc is None or len(desc) == 0:
        return np.empty((0, 128), dtype=np.float32)

    desc = desc.astype(np.float32)

    # (2) Hellinger: L1 then signed sqrt, no L2 afterwards
    l1 = np.sum(np.abs(desc), axis=1, keepdims=True)
    l1[l1 == 0] = 1.0
    desc = desc / l1
    desc = np.sign(desc) * np.sqrt(np.abs(desc))

    return desc

def loadDescsFromFile(path):
    if path.endswith(".pkl.gz"):
        with gzip.open(path, 'rb') as ff:
            return cPickle.load(ff, encoding='latin1')
    else:
        return computeDescs(path)
        

