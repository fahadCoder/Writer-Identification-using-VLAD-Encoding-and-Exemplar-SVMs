def getFiles(folder, pattern, labelfile):
    """ 
    returns files and associated labels by reading the labelfile 
    parameters:
        folder: inputfolder
        pattern: new suffix
        labelfiles: contains a list of filename and labels
    return: absolute filenames + labels 
    """

    """
  This function reads a labelfile containing file names and their associated class IDs, processes the filenames, 
  and returns a list of full file paths along with their respective labels. First, the labelfile is opened and its lines 
  are read. For each line, the filename and class ID are extracted by splitting the line. The function then iterates through 
  a predefined set of file extensions (.pkl.gz, .txt, .png, etc.) and removes any matching extension from the filename. 
  After that, a new filename is created by appending a specified suffix (pattern) to the filename, and the full file path 
  is generated by combining the given folder path and the new filename. The function then appends this full path to a list 
  of files and the class ID to a list of labels. Finally, the function returns these two lists: one containing the full 
  paths of the files and the other containing the corresponding class IDs.
    """


    # read labelfile
    with open(labelfile, 'r') as f: # Open the labelfile
        all_lines = f.readlines()   # Read all lines
    
    # get filenames from labelfile
    all_files = []  # Initialize list for all files
    labels = []     # Initialize list for all labels
    for line in all_lines:      # Iterate over all lines
        # using shlex we also allow spaces in filenames when escaped w. ""
        splits = shlex.split(line)  # Split the line
        file_name = splits[0]       # Get the filename
        class_id = splits[1]        # Get the class ID

        # strip all known endings, note: os.path.splitext() doesnt work for
        # '.' in the filenames, so let's do it this way...
        for p in ['.pkl.gz', '.txt', '.png', '.jpg', '.tif', '.ocvmb','.csv']:  # Iterate over all file extensions
            if file_name.endswith(p):
                file_name = file_name.replace(p,'') # Replace the file extension

        # get now new file name
        true_file_name = os.path.join(folder, file_name + pattern)      # Get the full path
        all_files.append(true_file_name)        # Append the full path to the list of all files
        labels.append(class_id)         # Append the class ID to the list of labels

    return all_files, labels # Return the list of all files and labels

